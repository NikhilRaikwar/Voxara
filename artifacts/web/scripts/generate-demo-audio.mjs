// One-off generator for the zero-upload demo track.
//
// Voxara never bundles copyrighted recordings. This script builds a fully
// rights-clear demo from the PUBLIC-DOMAIN folk song "Frère Jacques":
//   - synthesizes the audio ourselves via ElevenLabs TTS (with character-level
//     timestamps) so we own the recording and get exact word timing, and
//   - emits artifacts/web/public/demo/frere-jacques.mp3 plus the typed
//     session data in artifacts/web/src/data/demoTrack.ts.
//
// Re-run with:  ELEVENLABS_API_KEY=... node artifacts/web/scripts/generate-demo-audio.mjs
import { writeFile, mkdir } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = path.resolve(__dirname, "..");

const VOICE_ID = (
  process.env.ELEVENLABS_VOICE_ID || "21m00Tcm4TlvDq8ikWAM"
).trim();
const API_KEY = process.env.ELEVENLABS_API_KEY;
if (!API_KEY) {
  console.error("ELEVENLABS_API_KEY is not set");
  process.exit(1);
}

// Source: public-domain French nursery rhyme + literal English translations.
const LINES = [
  {
    text: "Frère Jacques, Frère Jacques,",
    translation: "Brother Jacques, Brother Jacques,",
  },
  {
    text: "Dormez-vous? Dormez-vous?",
    translation: "Are you sleeping? Are you sleeping?",
  },
  {
    text: "Sonnez les matines, sonnez les matines,",
    translation: "Ring the morning bells, ring the morning bells,",
  },
  {
    text: "Ding, ding, dong. Ding, ding, dong.",
    translation: "Ding, ding, dong. Ding, ding, dong.",
  },
];

const fullText = LINES.map((l) => l.text).join("\n");

async function main() {
  const res = await fetch(
    `https://api.elevenlabs.io/v1/text-to-speech/${encodeURIComponent(VOICE_ID)}/with-timestamps?output_format=mp3_44100_128`,
    {
      method: "POST",
      headers: {
        "xi-api-key": API_KEY,
        "Content-Type": "application/json",
        Accept: "application/json",
      },
      body: JSON.stringify({
        text: fullText,
        model_id: "eleven_multilingual_v2",
      }),
    },
  );

  if (!res.ok) {
    console.error(
      "ElevenLabs TTS failed",
      res.status,
      await res.text().catch(() => ""),
    );
    process.exit(1);
  }

  const data = await res.json();
  const audio = Buffer.from(data.audio_base64, "base64");
  const align = data.alignment;
  if (!align?.characters) {
    console.error("No alignment returned");
    process.exit(1);
  }

  const chars = align.characters;
  const starts = align.character_start_times_seconds;
  const ends = align.character_end_times_seconds;

  // Walk the character alignment, splitting into words and tracking which
  // source line each word belongs to (incremented on every newline).
  const lines = LINES.map((l) => ({
    text: l.text,
    translation: l.translation,
    words: [],
  }));
  let lineIdx = 0;
  let cur = null; // { text, start, end }
  const flush = () => {
    if (cur && cur.text.length > 0) lines[lineIdx].words.push(cur);
    cur = null;
  };
  for (let i = 0; i < chars.length; i++) {
    const c = chars[i];
    if (c === "\n") {
      flush();
      if (lineIdx < lines.length - 1) lineIdx++;
      continue;
    }
    if (/\s/.test(c)) {
      flush();
      continue;
    }
    if (!cur) cur = { text: "", start: starts[i], end: ends[i] };
    cur.text += c;
    cur.end = ends[i];
  }
  flush();

  const round = (n) => Math.round(n * 1000) / 1000;
  const sessionLines = lines.map((l, index) => {
    const ws = l.words;
    const ts = ws.length ? ws[0].start : 0;
    const te = ws.length ? ws[ws.length - 1].end : 0;
    return {
      index,
      ts: round(ts),
      te: round(te),
      text: l.text,
      translation: l.translation,
      words: ws.map((w) => ({
        text: w.text,
        start: round(w.start),
        end: round(w.end),
      })),
    };
  });

  const audioDir = path.join(WEB_ROOT, "public", "demo");
  await mkdir(audioDir, { recursive: true });
  await writeFile(path.join(audioDir, "frere-jacques.mp3"), audio);

  const fileBody = `// AUTO-GENERATED by scripts/generate-demo-audio.mjs — do not edit by hand.
// A fully rights-clear, zero-upload demo track: the public-domain folk song
// "Frère Jacques", with audio we synthesized ourselves (ElevenLabs TTS) and
// exact word timings derived from the TTS character alignment. Bundling this
// lets a cold visitor experience Listen + Practice end-to-end with no upload,
// and uses no copyrighted recording or cached Musixmatch lyric content.
import type { Track, TrackSession } from "@workspace/api-client-react";

// Sentinel id — real Musixmatch track ids are large positives, so -1 never
// collides. Used to route the demo through bundled data instead of the API.
export const DEMO_TRACK_ID = -1;
const DEMO_AUDIO_PATH = "demo/frere-jacques.mp3";

export const demoTrack: Track = {
  trackId: DEMO_TRACK_ID,
  commontrackId: DEMO_TRACK_ID,
  trackName: "Frère Jacques",
  artistName: "Traditional (public domain)",
  albumName: "Voxara Demo",
  albumCoverUrl: null,
  hasLyrics: true,
  hasRichsync: true,
  hasSubtitles: true,
  hasTranslation: true,
};

export const demoSession: TrackSession = {
  track: demoTrack,
  lines: ${JSON.stringify(sessionLines, null, 2).replace(/\n/g, "\n  ")},
  plainLyrics: null,
  copyright: "“Frère Jacques” is a traditional song in the public domain. Audio synthesized by Voxara via ElevenLabs.",
  hasRichsync: true,
  hasTranslation: true,
  syncLevel: "word",
  targetLanguage: "en",
};

export function isDemoTrack(track: { trackId: number } | null | undefined): boolean {
  return !!track && track.trackId === DEMO_TRACK_ID;
}

// Resolved against the artifact base path (Vite injects the trailing slash).
export function demoAudioUrl(): string {
  return \`\${import.meta.env.BASE_URL}\${DEMO_AUDIO_PATH}\`;
}
`;

  const dataDir = path.join(WEB_ROOT, "src", "data");
  await mkdir(dataDir, { recursive: true });
  await writeFile(path.join(dataDir, "demoTrack.ts"), fileBody);

  console.log("Wrote demo audio (%d bytes) and demoTrack.ts", audio.length);
  console.log(
    "Lines:",
    sessionLines.map((l) => `${l.ts}-${l.te}s ${l.words.length}w`).join(" | "),
  );
}

main();
